import SwiftUI

struct mainView: View {
    var body: some View {
        NavigationStack {
            VStack {
                Text("Hello, World!")

                NavigationLink(destination: NavigationView()) {
                    Text("다음 화면으로 이동")
                        .padding()
                        .background(Color.blue)
                        .foregroundColor(.white)
                        .cornerRadius(8)
                }
            }
            .navigationTitle("Main View")
        }
    }
}

